package com.apk.manager.beans;

import java.util.Date;

public class ApkBean {
	
	private String apkName;
	private String apkVer;
	private String apkPath;
	private String apkOwner;
	private Date apkRelDate;
	private String apkStatus;
	
	
	public String getApkName() {
		return apkName;
	}
	public void setApkName(String apkName) {
		this.apkName = apkName;
	}
	public String getApkVer() {
		return apkVer;
	}
	public void setApkVer(String apkVer) {
		this.apkVer = apkVer;
	}
	public String getApkPath() {
		return apkPath;
	}
	public void setApkPath(String apkPath) {
		this.apkPath = apkPath;
	}
	public String getApkOwner() {
		return apkOwner;
	}
	public void setApkOwner(String apkOwner) {
		this.apkOwner = apkOwner;
	}
	public Date getApkRelDate() {
		return apkRelDate;
	}
	public void setApkRelDate(Date apkRelDate) {
		this.apkRelDate = apkRelDate;
	}
	public String getApkStatus() {
		return apkStatus;
	}
	public void setApkStatus(String apkStatus) {
		this.apkStatus = apkStatus;
	}
	
	
	

}
